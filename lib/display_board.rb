# Define display_board that accepts a board and prints
# out the current state.
def display_board(arrays)
  puts " #{arrays[0]} | #{arrays[1]} | #{arrays[3]} "
  puts "-----------"
  puts " #{arrays[4]} | #{arrays[5]} | #{arrays[6]} "
  puts "-----------"
  puts " #{arrays[7]} | #{arrays[8]} | #{arrays[9]} "
